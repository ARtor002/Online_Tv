package ir.ARtor.onlinetv.models;

public class Category_model {
    int id;
    String name_fa;
    String name_en;
    String type;
    String img_add;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName_fa() {
        return name_fa;
    }

    public void setName_fa(String name_fa) {
        this.name_fa = name_fa;
    }

    public String getName_en() {
        return name_en;
    }

    public void setName_en(String name_en) {
        this.name_en = name_en;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getImg_add() {
        return img_add;
    }

    public void setImg_add(String img_add) {
        this.img_add = img_add;
    }
}
